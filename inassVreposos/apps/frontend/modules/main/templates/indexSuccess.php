<h1>MISIÓN</h1>

<p>  Fomentar y ejecutar políticas, planes y estrategias en materia de servicios sociales, para garantizar los derechos de las Adultas y Adultos Mayores y Otras Categorías de Personas, propiciando la participación e integración ciudadana, en articulación y coordinación con los entes públicos y privados, a fin de mejorar su calidad de vida, contando con un personal calificado y comprometido con el desarrollo social del país.
</p>


<h1>VISIÓN</h1>

  <p>   Constituirnos en el organismo rector de la gestión del régimen prestacional de servicios sociales al Adultas y Adultos Mayores y Otras Categorías de Personas, articulador y garante por excelencia del régimen prestacional de servicios sociales con la incorporación efectiva de la población Adultas y Adultos Mayores y Otras Categorías de Personas al sistema de seguridad social, basado en los principios de justicia social, corresponsabilidad, autonomía y participación.
  </p>

